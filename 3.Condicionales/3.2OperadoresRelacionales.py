# 1. Operadores RELACIONALES
num1 = 50
num2 = 10
num3 = 150
num4 = 150

# OPERADOR Igual que..
print ("Es {} Igual que {}?".format(num1,num2),num1 == num2)
print ("Es {} Igual que {}?".format(num3,num4),num3 == num4)
# OPERADOR Diferente de..
print ("Es {} Diferente de {}?".format(num1,num2),num1 != num2)
# OPERADOR Menor que..
print ("Es {} Menor que {}?".format(num1,num2),num1 < num2)
# OPERADOR Mayor que..
print ("Es {} Mayor que {}?".format(num1,num2),num1 > num2)
# OPERADOR Menor ó Igual a..
print ("Es {} Menor ó Igual a {}?".format(num1,num2),num1 <= num2)
print ("Es {} Mayor ó Igual a {}?".format(num3,num4),num3 <= num4)
# OPERADOR Mayor ó Igual a..
print ("Es {} Mayor ó Igual a {}?".format(num1,num2),num1 >= num2)
print ("Es {} Mayor ó Igual a {}?".format(num3,num4),num3 >= num4)

