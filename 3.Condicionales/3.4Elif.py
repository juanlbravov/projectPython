num1 = int(input("Define un numero de 1 a 5: "))

if (num1 == 1):
    print("El numero {} es el Uno".format(num1))
elif (num1 == 2):
    print("El numero {} es el Dos".format(num1))
elif (num1 == 3):
    print("El numero {} es el Trea".format(num1))
elif (num1 == 4):
    print("El numero {} es el Cuatro".format(num1))
elif (num1 == 5):
    print("El numero {} es el Cinco".format(num1))
else:
    print("El numero no cumple las condiciones requeridas")

